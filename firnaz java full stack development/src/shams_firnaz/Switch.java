package shams_firnaz;

public class Switch {
	public static void main(String args[]) {
		int t=2;
		switch(t) {
		case 1:
		{
		System.out.println("java");
		break;
		}
		case 2:
		{
		System.out.println("python");
		break;
		}
		default:
		{
			System.out.println("there is nothing");
			break;
		}
		}
	}

}
